//Write a program to count down from 10 to 0, printing each number. 

public class Sec3Q8 {
    public static void main(String[] args) {
        System.out.println("Countdown from 10 to 0:");
        
        for (int i = 10; i >= 0; i--) {
            System.out.println(i);
        }
    }
}


